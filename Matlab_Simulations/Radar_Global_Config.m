%% 车载雷达全局参数配置文件
% 此文件定义了后续所有仿真通用的物理常数和雷达系统参数

% 1. 物理常数
c = 3e8;                % 光速 (m/s)

% 2. 典型车载雷达参数 (77 GHz 频段)
fc = 77e9;              % 载频 (Hz)
lambda = c / fc;        % 波长 (m)

% 3. FMCW 波形参数 (示例)
B = 150e6;              % 带宽 (Hz)，决定距离分辨率
Tc = 10e-6;             % 扫频时间 (s)
Slope = B / Tc;         % 扫频斜率 (Hz/s)

fprintf('雷达全局参数已加载。波长: %.4f mm\n', lambda * 1000);
